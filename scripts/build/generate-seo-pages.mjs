function sitemap() {
  const entries = [
    { pathname: "/" },
    { pathname: "/about.html" },
    { pathname: "/contact.html" },
    { pathname: "/designs.html" },
    { pathname: "/collections/" },
    ...collections.map((collection) => ({ pathname: `/collections/${collection.slug}/` })),
    { pathname: "/guides/" },
    ...content.guides.map((guide) => ({ pathname: `/guides/${guide.slug}/` })),
    ...groups.map((group) => ({
      pathname: `/designs/${group.slug}/`,
      images: unique(group.variants.map((variant) => absolute(`/${variant.mockupImage}`)))
    }))
  ];
  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9" xmlns:image="http://www.google.com/schemas/sitemap-image/1.1">
${entries.map((entry) => `  <url>
    <loc>${escapeHtml(absolute(entry.pathname))}</loc>
    <lastmod>${lastmod}</lastmod>${entry.images ? entry.images.map((image) => `
    <image:image><image:loc>${escapeHtml(image)}</image:loc></image:image>`).join("") : ""}
  </url>`).join("\n")}
</urlset>
`;
}

