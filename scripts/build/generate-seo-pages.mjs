function breadcrumb(items) {
  return `<nav class="breadcrumbs" aria-label="Breadcrumb">
      <ol>${items.map((item, index) => `<li>${item.href && index < items.length - 1 ? `<a href="${item.href}">${escapeHtml(item.name)}</a>` : `<span aria-current="page">${escapeHtml(item.name)}</span>`}</li>`).join("")}</ol>
    </nav>`;
}

function breadcrumbSchema(items) {
  return {
    "@type": "BreadcrumbList",
    itemListElement: items.map((item, index) => ({
      "@type": "ListItem",
      position: index + 1,
      name: item.name,
      item: item.pathname ? absolute(item.pathname) : undefined
    }))
  };
}

